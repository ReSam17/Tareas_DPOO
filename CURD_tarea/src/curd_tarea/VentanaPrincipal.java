    private void jButtonCargarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButtonCargarActionPerformed
        Cargar dialog = new Cargar(this, true);

        boolean aceptado = dialog.exec();
        if (aceptado == true) {
            String s = dialog.getS();
            if (s.trim().equals("") || s.isEmpty() == true) {
           }else{
            try {
                this.controlador.setGrupo(this.controlador.Importar(this.controlador.getGrupo(), s));
               } catch (ClassNotFoundException | IOException e) {
               }
               actualizarFooter();
               actualizarTabla();
           }
       }
    }// GEN-LAST:event_jButtonCargarActionPerformed

    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButtonGuardarActionPerformed
        Guardado diaolog = new Guardado(this, true);

        boolean acepted = diaolog.exec();
        if (acepted == true) {
            String s = diaolog.getS();
            if (s.trim().equals("") || s.isEmpty() == true) {
            } else {
                try {
                    controlador.Exportar(controlador.getGrupo(), s);

                } catch (IOException e) {
                }
            }
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

