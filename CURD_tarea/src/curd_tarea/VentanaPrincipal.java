    private void actualizarFooter() {
        String s=String.valueOf(controlador.getGrupo().length);
        if (s.equals("")||s.equals(" ")||s.equals(null)||s.equals("0")) {
            jLabelCantiddaPersonas.setText("0");
        }else{
            jLabelCantiddaPersonas.setText(s);
        }
    }
    private void actualizarTabla() {
        Object [] nombreCol= {"Nombre","Edad","Altrua"};
 
        DefaultTableModel model=new DefaultTableModel(nombreCol, 0);

        for (int i = 0; i < this.controlador.getGrupo().length ; i++) {

            String name=this.controlador.getGrupo()[i].getName();
            String age=String.valueOf(this.controlador.getGrupo()[i].getEdad());
            String altrua=Double.toString(this.controlador.getGrupo()[i].getAltura());
            Object [] fila={name,age,altrua};
            model.addRow(fila);
        }
        jTable1.setModel(model);
    }
    private boolean validarEliminacion(){
        boolean vali=true;
        if(jTextFieldEliminacion.getText().trim().equals("")){
            vali=false;
        }
        if( this.controlador.getGrupo().length>0){
            for (int i = 0; i < this.controlador.getGrupo().length; i++) {
                  if(jTextFieldEliminacion.getText().trim().equals(this.controlador.getGrupo()[i].getName().trim())){
                      vali=true;
                      return vali;
                  }else{
                      vali=false;
                  }
            }
        }else{
            vali=false;
        }
        
        return vali;
    }
